class Colours():
	def pink(s):
	    return "\033[1;95m" + s +  "\033[00m"

	def cyan(s):
	    return "\033[1;96m" + s +  "\033[00m"

	def yellow(s):
	    return "\033[1;93m" + s +  "\033[00m"

	def red(s):
	    return "\033[1;91m" + s +  "\033[00m"

	def green(s):
	    return "\033[1;92m" + s +  "\033[00m"